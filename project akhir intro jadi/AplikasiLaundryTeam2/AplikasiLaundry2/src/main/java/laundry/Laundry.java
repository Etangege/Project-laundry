            list.add(jLabel4.getText());
            list.add(txtJmlPcs.getText() + " pcs");
            list.add(cmbJemput.getSelectedItem().toString());
            dataModel.addRow(list.toArray());
            Sum();
            
            //txtTglTransaksi.setDateFormatString("");
            cmbKodeCust.setSelectedIndex(0);
            txtNmCust.setText("");
            areaAlamat.setText("");            
            txtHp.setText("");
            txtJmlPcs.setText("");
