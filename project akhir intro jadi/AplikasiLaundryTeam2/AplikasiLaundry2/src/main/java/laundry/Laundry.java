    private void txtHpKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtHpKeyTyped
        // TODO add your handling code here:
        //HARUS ANGKA UNTUK NO HP lebih dari 6 digit dan kurang dari sama dengan 12
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9') && txtHp.getText().length() < 12 || (c == KeyEvent.VK_BACK_SPACE)|| (c == KeyEvent.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
}

    }//GEN-LAST:event_txtHpKeyTyped

