            cmbBerat.setSelectedIndex(0);
            txtHarga.setText("");
            jLabel3.setText("Rp 0,00");
            jLabel4.setText("Rp 0,00");
            //txtEstAmbil.setDateFormatString("");
            cmbJemput.setSelectedIndex(0);
            txtTotal.setText("");
